; ------------------------------------------------------------------------------
; SP48 API Macros
; ------------------------------------------------------------------------------
; ------------------------------------------------------------------------------
; Sign error
; ------------------------------------------------------------------------------
SIGN_ERR .macro()
    ld a,2
    out ($fe),a
.endm

; ------------------------------------------------------------------------------
; Clear Screen
; ------------------------------------------------------------------------------
ClearScreen
    push hl
    push de
    push bc
    ld hl,$4000
    ld de,$4001
    ld bc,$17ff
    ld (hl),0
    ldir
    ld hl,$5800
    ld de,$5801
    ld bc,$02ff
    ld (hl),$38
    ldir
    pop bc
    pop de
    pop hl
    ret

; ------------------------------------------------------------------------------
; PrintText
; ------------------------------------------------------------------------------
PrintText .macro(addr, length)
    ld de,{{addr}}
    ld bc,{{length}}
    call $203c
.endm